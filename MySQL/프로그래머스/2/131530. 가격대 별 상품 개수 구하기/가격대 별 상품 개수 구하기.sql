SELECT 
CASE 
    WHEN PRICE < 10000 THEN '0'
    WHEN PRICE >= 10000 THEN SUBSTRING(PRICE, 1, 1)*10000
    ELSE NULL
END AS PRICE_GROUP, 
COUNT(SUBSTRING(PRICE, 1, 1)*10000) AS PRODUCTS

FROM PRODUCT

GROUP BY PRICE_GROUP
ORDER BY PRICE_GROUP
